package org.gissolutions.jsimpleutils.utils;

public class ObjectCloningException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5194716649590193243L;

	public ObjectCloningException() {
	
	}

	public ObjectCloningException(String message) {
		super(message);
		
	}

	public ObjectCloningException(Throwable cause) {
		super(cause);
		
	}

	public ObjectCloningException(String message, Throwable cause) {
		super(message, cause);
		
	}

}
